        private void carregarDados(){
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conectado = DriverManager.getConnection("jdbc:mysql://localhost:3307/consultorio", "root", "p@$$w0rd");

            PreparedStatement st = conectado.prepareStatement("SELECT * FROM paciente");
            ResultSet paciente = st.executeQuery();
            DefaultTableModel tblModelo = (DefaultTableModel) tblPacientes.getModel();
            
            //Percorrendo toda os usu√°rios presentes no BD, para depois colocarmos num modelo e 
            while(paciente.next()){     
                Object linha[] = {
                    paciente.getString("nome"),
                    paciente.getString("cpf"),
                    paciente.getString("telefone"),
                    paciente.getString("endereco"),
                    paciente.getString("cep"),
                    paciente.getString("bairro"),
                    paciente.getString("complemento"),
                    paciente.getString("numero")
                };
                tblModelo.addRow(linha);
            }
        } catch (ClassNotFoundException | java.sql.SQLException ex) {
            JOptionPane.showMessageDialog(null, "Entre em contato com o suporte e informe o erro: "+ex.getMessage());
        }
            
    }

